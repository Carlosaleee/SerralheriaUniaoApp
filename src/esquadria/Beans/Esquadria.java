package esquadria.Beans;

    public class Esquadria {
    
        private int idEsquadria;
        private String nomeEsquadria;
        private String descricao;
        private String preco;
        private String distribuidorId;

    public int getIdEsquadria() {
        return idEsquadria;
    }

    public void setIdEsquadria(int idEsquadria) {
        this.idEsquadria = idEsquadria;
    }

    public String getNomeEsquadria() {
        return nomeEsquadria;
    }

    public void setNomeEsquadria(String nomeEsquadria) {
        this.nomeEsquadria = nomeEsquadria;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getPreco() {
        return preco;
    }

    public void setPreco(String preco) {
        this.preco = preco;
    }

    public String getDistribuidorId() {
        return distribuidorId;
    }

    public void setDistribuidorId(String distribuidorId) {
        this.distribuidorId = distribuidorId;
    }

    
        

}

