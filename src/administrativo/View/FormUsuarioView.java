     private void preencherTabela() {
                
            UsuariosDao usuariosDao = new UsuariosDao();

            List<Usuario> listaUsuarios = usuariosDao.getUsuario();

            DefaultTableModel tabelaUsuarios = (DefaultTableModel) tblUsuario.getModel();

            tblUsuario.setRowSorter(new TableRowSorter(tabelaUsuarios));

            for (Usuario u : listaUsuarios) { //em cada volta do laço for, o mesmo adiciona uma dado(empresa) dentro do objeto c
                Object[] obj = new Object[]{
                    u.getIdUsuario(), 
                    u.getLogin(),
                    u.getSenha(),
                    u.getSenha(),            
                };
                tabelaUsuarios.addRow(obj);
            }
        }
        
        
        
